package shestak.maksym.schedule;

public class Class {
    String title;
    String type;
    String auditorium;
    String lecturer;
    String group;
    String time;
    String classN;
    String date;

    public Class(String title, String type, String auditorium, String lecturer, String group, String time, String classN, String date) {
        this.title = title;
        this.type = type;
        this.auditorium = auditorium;
        this.lecturer = lecturer;
        this.group = group;
        this.time = time;
        this.classN = classN;
        this.date = date;
    }
}
